/**
 * Infer a Wikipedia article title from question content.
 */
function inferArticle(q, domain) {
  const text = (q.question_text || '') + ' ' + Object.values(q.options || {}).join(' ');
  const concepts = (q.concepts_tested || []).join(' ');
  const correctText = (q.options || {})[q.correct_answer] || '';
  const fullText = text + ' ' + concepts + ' ' + correctText;

  // Try global rules first
  for (const rule of GLOBAL_RULES) {
    if (rule.p.test(fullText)) {
      return typeof rule.a === 'function' ? rule.a(fullText.match(rule.p), q, domain) : rule.a;
    }
  }

  // Fallback: use concepts_tested + domain to construct a reasonable article title
  if (q.concepts_tested && q.concepts_tested.length > 0) {
    const concept = q.concepts_tested[0];
    // Clean up and capitalize appropriately
    const title = concept
      .split(/[\s_]+/)
      .map((w, i) => {
        // Capitalize first word, keep rest lowercase unless it's a proper noun indicator
        if (i === 0) return w.charAt(0).toUpperCase() + w.slice(1);
        return w;
      })
      .join(' ');
    return title;
  }

  // Last resort: use domain name
  const domainTitle = domain
    .split('-')
    .map((w, i) => {
      if (i === 0) return w.charAt(0).toUpperCase() + w.slice(1);
      return w;
    })
    .join(' ');
  return domainTitle;

}

// ── Main ──────────────────────────────────────────────────────────────

const files = readdirSync(domainsDir)
  .filter(f => f.endsWith('.json') && f !== 'index.json');

let totalFixed = 0;
const filesModified = [];

for (const file of files) {
  const filePath = join(domainsDir, file);
  const domain = file.replace('.json', '');
  const data = JSON.parse(readFileSync(filePath, 'utf-8'));

  if (!Array.isArray(data.questions)) continue;

  let modified = false;
  for (const q of data.questions) {
    if (!q.source_article || typeof q.source_article !== 'string' || q.source_article.trim() === '') {
      const article = inferArticle(q, domain);
      q.source_article = article;
      totalFixed++;
      modified = true;
    }
  }

  if (modified) {
    writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n');
    filesModified.push(file);
  }
}

console.log(`Fixed ${totalFixed} questions across ${filesModified.length} files:`);
for (const f of filesModified) {
  console.log(`  - ${f}`);
}
